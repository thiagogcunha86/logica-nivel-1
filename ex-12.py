#12 - Escreva um algoritmo para ler as dimensões de um retângulo (base e altura), calcular e escrever a área do retângulo.

#Dimensões do retângulo
base = float(input("Digite o valor da base do retângulo: "))
altura = float(input("Digite o valor da altura do retângulo: "))

# Calculo Area Total
area = base * altura

# Resultado
print(f"A área do retângulo é: {area}")
